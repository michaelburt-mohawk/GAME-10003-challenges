/// <summary>
///     Defines mouse buttons.
/// </summary>
/// <remarks>
///     Raylib's MouseButton
/// </remarks>
public enum MouseButton
{
    /// <summary>
    ///     Left mouse button.
    /// </summary>
    Left = 0,

    /// <summary>
    ///     Right mouse button.
    /// </summary>
    Right = 1,

    /// <summary>
    ///     Middle mouse button.
    /// </summary>
    Middle = 2,

    /// <summary>
    ///     Side mouse button.
    /// </summary>
    Side = 3,

    /// <summary>
    ///     Extra mouse button extra.
    /// </summary>
    Extra = 4,

    /// <summary>
    ///     Forward mouse button.
    /// </summary>
    Forward = 5,

    /// <summary>
    ///     Back mouse button.
    /// </summary>
    Back = 6
}
